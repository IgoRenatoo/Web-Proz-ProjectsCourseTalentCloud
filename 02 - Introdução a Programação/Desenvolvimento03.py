# Estrutura de repetição FOR
for i in range(1, 21):
    if i != 13:
        print(f"Estrutura FOR: {i}")


# Estrutura de repetição WHHILE
i = 1
while i < 21:
  if i != 13:
     print(f"Estrutura WHHILE: {i}")
  i+=1


# Estrutura de repetição FOR decrescente
for i in range(20, 0, -1):
    if i != 13:
        print(f"Estrutura FOR decrescente: {i}")
